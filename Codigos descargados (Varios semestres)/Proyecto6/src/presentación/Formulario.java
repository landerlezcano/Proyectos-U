    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Carro aux = objPar.getObjCarro();
        aux.setPlaca(placa.getText());
        if(Integer.parseInt(he.getText())>Integer.parseInt(hs.getText())){
            JOptionPane.showMessageDialog(rootPane, "隆Error! La hora de entrada debe ser menor que la de salida.");
            he.setText("");
            me.setText("");
            se.setText("");
            hs.setText("");
            ms.setText("");
            ss.setText("");

        }else if(he.getText().trim().isEmpty() || me.getText().trim().isEmpty()|| 
                se.getText().trim().isEmpty()|| hs.getText().trim().isEmpty() || 
                ms.getText().trim().isEmpty() || ss.getText().trim().isEmpty() || 
                valorM.getText().trim().isEmpty() || placa.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "隆Error! Aseg煤rese de rellenar todos los campos.");
            he.setText("");
            me.setText("");
            se.setText("");
            hs.setText("");
            ms.setText("");
            ss.setText("");
            valorM.setText("");
        }else{
            objPar.setHoraEntrada(new Tiempo(Integer.parseInt(he.getText()),
            Integer.parseInt(me.getText()), Integer.parseInt(se.getText())));
            objPar.setHoraSalida(new Tiempo(Integer.parseInt(hs.getText()),
            Integer.parseInt(ms.getText()), Integer.parseInt(ss.getText())));
            objPar.setValorMin(Integer.parseInt(valorM.getText()));
            String aux1="";
            aux1+="Placa del veh铆culo: '"+objPar.objCarro.getPlaca()+"'\nHora de entrada: "+objPar.getHoraEntrada().toString()+"\n";
            aux1+="Hora de salida: "+objPar.getHoraSalida().toString()+"\n"+"Valor minuto: "+objPar.getValorMin()+"\n";
            aux1+="\nValor total a pagar: "+objPar.calcularTarifa();
            campoResult.setText(aux1);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

