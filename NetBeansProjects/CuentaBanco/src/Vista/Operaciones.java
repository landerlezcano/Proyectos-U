    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Long monto = Long.parseLong(JOptionPane.showInputDialog(rootPane, 
                    "Ingrese el monto en efecto a consignar en su cuenta:"));
            JOptionPane.showMessageDialog(rootPane, (cuenta.Consignar(monto)));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "!Error¡ Digite solo números sin símbolos");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

