    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        long cantidad = 0;
        try{
        cantidad = Long.parseLong(JOptionPane.showInputDialog(rootPane, "Ingrese la cantidad de dinero a retirar:"));
        }catch(Exception e){
            e.printStackTrace();
            e.getMessage();
            cantidad=-1;
        }
        while(cantidad<=0){
            try{
                cantidad = Long.parseLong(JOptionPane.showInputDialog(rootPane, "¡ERROR! Ingrese una cantidad válida de dinero a retirar:"));
            }catch(Exception e){
                e.printStackTrace();
                e.getMessage();
                cantidad=-1;
            }
        }
        if(objAux.getClass().toString().equals("class lógica.CuentaAhorro")){
            CuentaAhorro tmp = (CuentaAhorro)objAux;
            tmp.retiro(cantidad);
        }else if(objAux.getClass().toString().equals("class lógica.CuentaCorriente")){
            CuentaCorriente tmp = (CuentaCorriente)objAux;
            tmp.retiro(cantidad);
        }else{
            System.out.println("ERROR consulta cuenta clase no encontrada");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

