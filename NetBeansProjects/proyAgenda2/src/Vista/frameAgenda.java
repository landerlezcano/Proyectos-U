    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        // TODO add your handling code here:
        String aux2 = JOptionPane.showInputDialog("Digite el ID del contacto");
        for(int i=0;i<objA.size();i++){
            Contacto aux1 = (Contacto)objA.get(i);
            if(aux2.equals(aux1.getId())){
                JOptionPane.showMessageDialog(this,"Datos del Contacto:\n"+aux1.toString());
                break;
            }
        }
        jtaDatos.setText("");
    }//GEN-LAST:event_btnbuscarActionPerformed

