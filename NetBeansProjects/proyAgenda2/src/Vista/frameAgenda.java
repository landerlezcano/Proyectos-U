    private void btningrsarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningrsarActionPerformed
        // TODO add your handling code here:
        Contacto aux = new Contacto();
        btnNuevo.setEnabled(true);
        aux.setId(this.txtid.getText());
        aux.setNombre(this.txtnombre.getText());
        aux.setTel(this.txttel.getText());
        aux.setEmail(this.txtemail.getText());
        if(txtid.getText().trim().equals("")  || txtnombre.getText().trim().equals("")
                || txttel.getText().trim().equals("") || txtemail.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Error, asegúrese de rellenar todos los campos");
        }else{
        objA.add(aux);
        JOptionPane.showMessageDialog(this,"Ingresado correctamente");
        }
        Inhabilitar();
        btnMostrar.setEnabled(true);
        btnbuscar.setEnabled(true);
    }//GEN-LAST:event_btningrsarActionPerformed

