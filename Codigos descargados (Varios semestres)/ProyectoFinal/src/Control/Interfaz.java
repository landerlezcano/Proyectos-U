    private String actualizarTabla(ArrayList lista){
        String tmp = "C칩digo\t"+"Nombre\t"+"ISBN\t"+"Autor\t"+"Edici칩n\t"
                +"Editorial\t"+"Fecha de publicaci칩n\n\n";
        if(!lista.isEmpty()){
            for (int i = 0;i<lista.size();i++){
                Libro aux = (Libro)lista.get(i);
                String[] aux2 = aux.toArrayString();
                for(String foo:aux2){
                    tmp=tmp+foo+"\t";
                }
                tmp=tmp+"\n";
            }
        }
        return tmp;
    }
    
