    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if(jComboBox1.getSelectedIndex()==0){
            CuentaAhorro objAux = new CuentaAhorro();
            objAux.setTitular(jTextField2.getText());
            objAux.setNo_cuenta(Long.parseLong(jTextField1.getText()));
            objAux.setClave(jPasswordField1.getText());
            objAux.setSaldo(Long.parseLong(jTextField3.getText()));
            objAux.setCuotaManejo(Long.parseLong(jTextField5.getText()));
            JOptionPane.showMessageDialog(rootPane, "¡Cuenta de ahorro creada!");
            arrayAux.add(objAux);
        }else if(jComboBox1.getSelectedIndex()==1){
            CuentaCorriente objAux = new CuentaCorriente();
            objAux.setTitular(jTextField2.getText());
            objAux.setNo_cuenta(Long.parseLong(jTextField1.getText()));
            objAux.setClave(jPasswordField1.getText());
            objAux.setSaldo(Long.parseLong(jTextField3.getText()));
            objAux.setSobregiro(Long.parseLong(jTextField4.getText()));
            JOptionPane.showMessageDialog(rootPane, "¡Cuenta corriente creada creada!");
            arrayAux.add(objAux);
        }
        
        this.setVisible(false);
        this.jTextField2.setText("");
        this.jTextField1.setText("");
        this.jTextField3.setText("");
        this.jTextField4.setText("");
        this.jTextField5.setText("");
        this.jPasswordField1.setText("");
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if(jComboBox1.getSelectedIndex()==0){
                jLabel11.setEnabled(false);
                jLabel11.setVisible(false);
                jTextField4.setEnabled(false);
                jTextField4.setVisible(false);
                jLabel10.setEnabled(true);
                jLabel10.setVisible(true);
                jTextField5.setEnabled(true);
                jTextField5.setVisible(true);
        }else if(jComboBox1.getSelectedIndex()==1){
                jLabel11.setEnabled(true);
                jLabel11.setVisible(true);
                jTextField4.setEnabled(true);
                jTextField4.setVisible(true);
                jLabel10.setEnabled(false);
                jLabel10.setVisible(false);
                jTextField5.setEnabled(false);
                jTextField5.setVisible(false);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

