    private void AsignarMT(){
    
        TablaE = new DefaultTableModel();
         TablaV = new DefaultTableModel();
         String Titulos[] = {"Codigo_Producto","Nombre_Producto","Precio_Unidad"};
         this.TablaE.setColumnIdentifiers(Titulos);
         this.TablaV.setColumnIdentifiers(Titulos);
         this.JTElectro.setModel(TablaE);
         this.JTViveres.setModel(TablaV);
         Object[] Fila = new Object[TablaE.getColumnCount()];
         for(int i = 0 ; i < LElectro.size() ; i++){
             Fila[0] = LElectro.get(i).getCodigo();
             Fila[1] = LElectro.get(i).getNombre();
             Fila[2] = LElectro.get(i).getValor_Unidad();
             TablaE.addRow(Fila);
         }
         Object[] Fila1 = new Object[TablaV.getColumnCount()];
         for(int i = 0 ; i < LViveres.size() ; i++){
             Fila1[0] = LViveres.get(i).getCodigo();
             Fila1[1] = LViveres.get(i).getNombre();
             Fila1[2] = LViveres.get(i).getValor_Unidad();
             TablaV.addRow(Fila1);
             
         }
}
