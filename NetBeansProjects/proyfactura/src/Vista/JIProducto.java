    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        if(txtCodigoP.getText().trim().length()== 0 ||
                txtNombreP.getText().trim().length()== 0 ||
                txtValU.getText().trim().length()== 0 ||
                CboxTipo.getSelectedIndex() == 0){
            
        JOptionPane.showMessageDialog(this, "Complete todo el formulario");
        }
        
        else{
            String Datos1[] = { this.txtCodigoP.getText(),  this.txtNombreP.getText(), this.txtValU.getText()};
            try{    
            if(this.CboxTipo.getSelectedIndex() == 1){
                
                ObjE = new Electro();
                this.ObjE.setNombre(this.txtNombreP.getText());
                this.ObjE.setCodigo(this.txtCodigoP.getText());
                this.ObjE.setValor_Unidad(Double.parseDouble(this.txtValU.getText()));
                LElectro.add(ObjE);
                JOptionPane.showMessageDialog(this,"Los Datos del Producto Son : \n" + ObjE.toString());
                TablaE.addRow(Datos1);

                
            }
            else {
                
                ObjV = new Viveres();
                this.ObjV.setNombre(this.txtNombreP.getText());
                this.ObjV.setCodigo(this.txtCodigoP.getText());
                //this.ObjV.setCantidad(Integer.parseInt(this.txtCantP.getText()));
                this.ObjV.setValor_Unidad(Double.parseDouble(this.txtValU.getText()));
                LViveres.add(ObjV);
                JOptionPane.showMessageDialog(this,"Los Datos del Producto Son : \n" + ObjV.toString());
                TablaV.addRow(Datos1);
            }
        
        JOptionPane.showMessageDialog(this,"Producto Agregado");
                        }
                catch(NumberFormatException e){
                    JOptionPane.showMessageDialog(this, "Ingrese Un Precio Correcto");
                }
        txtCodigoP.setText("");
        txtNombreP.setText("");
        //txtCantP.setText("");
        txtValU.setText("");
        CboxTipo.setSelectedIndex(0);
        
        }
    }//GEN-LAST:event_btnCrearActionPerformed

