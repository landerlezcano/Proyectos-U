    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(jComboBox1.getSelectedIndex()==0){
            ProductoSoftware aux = new ProductoSoftware();
            if(jComboBox2.getSelectedIndex()==0){
                aux.setTipo(ProductoSoftware.tipoSoftware.Juego);
            }else if(jComboBox2.getSelectedIndex()==1){
                aux.setTipo(ProductoSoftware.tipoSoftware.SO);
            }else if(jComboBox2.getSelectedIndex()==2){
                aux.setTipo(ProductoSoftware.tipoSoftware.Office);
            }else if(jComboBox2.getSelectedIndex()==3){
                aux.setTipo(ProductoSoftware.tipoSoftware.otro);
            }
            aux.setID(Long.parseLong(jTextField5.getText()));
            aux.setNombre_producto(jTextField1.getText());
            aux.setMarca_producto(jTextField2.getText());
            aux.setPrecio_producto(Float.parseFloat(jTextField3.getText()));
            aux.setCantidad(Integer.parseInt(jTextField4.getText()));
            this.aux=aux;
        }else if(jComboBox1.getSelectedIndex()==1){
            ProductoHardware aux = new ProductoHardware();
            if(jComboBox3.getSelectedIndex()==0){
                aux.setTipo(ProductoHardware.tipoHardware.Portatil);
            }else if(jComboBox3.getSelectedIndex()==1){
                aux.setTipo(ProductoHardware.tipoHardware.PC_Escritorio);
            }else if(jComboBox3.getSelectedIndex()==2){
                aux.setTipo(ProductoHardware.tipoHardware.Servidor);
            }else if(jComboBox3.getSelectedIndex()==3){
                aux.setTipo(ProductoHardware.tipoHardware.otro);
            }
            aux.setID(Long.parseLong(jTextField5.getText()));
            aux.setNombre_producto(jTextField1.getText());
            aux.setMarca_producto(jTextField2.getText());
            aux.setPrecio_producto(Float.parseFloat(jTextField3.getText()));
            aux.setCantidad(Integer.parseInt(jTextField4.getText()));
            this.aux=aux;
        }
        
        JOptionPane.showMessageDialog(rootPane, "Producto agregado a la factura.\n\n"+aux.toString());
        listaProductos.add(aux);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        aux = null;
    }//GEN-LAST:event_jButton1ActionPerformed

