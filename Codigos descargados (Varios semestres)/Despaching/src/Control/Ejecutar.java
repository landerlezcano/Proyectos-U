    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(!reg.esVacia()){
            String tmp;
            Operacion aux;
            timestamp aux2 = new timestamp();
            Operacion ob1 = new Operacion();
            Object[] options = {"Entrada",
                        "Salida","Cancelar"};
            int opc = JOptionPane.showOptionDialog(null,"Borrar operación en cola de: ","Borrar operación",JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, 0);
            switch(opc){
                case 0:
                    if(!entrada.esVacia()){
                        aux = entrada.borrarOperacion();
                        ob1.mostrarOperacion(aux);
                        reg.agregar("Borrar operación Entrada\t"+aux2.makeTimestamp()+"\t"+aux.toString());
                    }else{
                        JOptionPane.showMessageDialog(null, "No se han agregado operaciones a la cola Entrada", "Cola vacia", JOptionPane.ERROR_MESSAGE);
                    }
                    break;
                case 1:
                    if(!salida.esVacia()){
                        aux = salida.borrarOperacion();
                        ob1.mostrarOperacion(aux);
                        reg.agregar("Borrar operación Salida\t"+aux2.makeTimestamp()+"\t"+aux.toString());
                    }else{
                        JOptionPane.showMessageDialog(null, "No se han agregado operaciones al registro", "Cola vacia", JOptionPane.ERROR_MESSAGE);
                    }
                    break;
                default:
                    break;
            }
            actualizarCajas();
        }else{
            JOptionPane.showMessageDialog(null, "No se han agregado operaciones al registro", "Registro vacio", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(!reg.esVacia()){
            InOut aux = new InOut();
            Vista.ValidacionDatos aux2 = new ValidacionDatos();
            int i = aux.solicitarNum("A continuación ingrese la ID de la operación a consultar: ");
            while(aux2.validarDato(i)==false){
                i = aux.solicitarNum("¡Error!\nA continuación ingrese la ID válida de la operación a consultar: ");
            }
            if(i>reg.tamañoRegistro() || i<0){
                JOptionPane.showMessageDialog(null, "El ID de operación ingresado ("+i+") no se encuentra en el registro.", "No encontrado", JOptionPane.ERROR_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, new JTextArea("ID\tSello de tiempo\t\tTipo de envio\tNo. de unidades\tFragilidad\tPeso\tFecha de operación\tLugar\n"+reg.elementoEn(i-1)), "Información de la operación ID: "+i, JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "No se han agregado operaciones al registro", "Registro vacio", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

