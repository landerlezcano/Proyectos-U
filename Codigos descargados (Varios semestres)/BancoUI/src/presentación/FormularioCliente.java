    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Boolean encontrado = false;
        for(Object obj1:arrayAux){
            CuentaBancaria aux1 = (CuentaBancaria)obj1;
            if(jComboBox1.getSelectedIndex()==0){
                if(aux1.getClass().toString().equals("class lógica.CuentaAhorro") && 
                aux1.auntenticarUsuario(jPasswordField1.getText(), Long.parseLong(jTextField1.getText()))               ){
                    
                    objAux=(CuentaAhorro)aux1;
                    JOptionPane.showMessageDialog(rootPane, "Acceso concedido.");
                    encontrado=true;
                }
            }else if(jComboBox1.getSelectedIndex()==1){
                if(aux1.getClass().toString().equals("class lógica.CuentaCorriente") && 
                aux1.auntenticarUsuario(jPasswordField1.getText(), Long.parseLong(jTextField1.getText()))              ){
                    
                    objAux=(CuentaCorriente)aux1;
                    JOptionPane.showMessageDialog(rootPane, "Acceso concedido.");
                    encontrado=true;
                }
            }
            if(encontrado) break;
        }
        if(!encontrado){
            JOptionPane.showMessageDialog(rootPane, "¡Error! Usuario no encontrado");
        }else{
            this.getContentPane().removeAll();
            this.getContentPane().repaint();
            this.getContentPane().revalidate();
            this.add(jPanel2);
            this.getContentPane().repaint();
            this.getContentPane().revalidate();
        }
        jPasswordField1.setText("");
        jTextField1.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

