    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(!listaProductos.isEmpty()){
            String auxFactura = "";
            float total=0;
            int idProducto = 0;
            for(Object objAux:listaProductos){
                idProducto++;
                Producto aux2 = (Producto)objAux;
                auxFactura+=("Producto no.: "+idProducto)+"\n"+objAux.toString()+"\n";
                System.out.println(aux2.precioIVA()+"   "+total);
                total+=((aux2.getPrecio_producto()+aux2.precioIVA())*aux2.getCantidad());
            }
            jTextArea1.setText(auxFactura+"\n\n\tTOTAL FACTURA: "+String.format("%,5.2f",total));
            jDialog1.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(rootPane, "No hay productos agregados en la factura.");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

